package hello;

//Greeting Object to be returned in Rest Call
//Unique Id in each rest call (new object each call)

public class Greeting {

    private final long id;
    private final String content;

    public Greeting(long id, String content) {
        this.id = id;
        this.content = content;
    }

    public long getId() {
        return id;
    }

    public String getContent() {
        return content;
    }
}
